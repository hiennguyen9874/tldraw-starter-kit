import { writeFile } from 'node:fs/promises'
import { fileURLToPath } from 'node:url'
import { createServer } from 'vite'

const outputPath = fileURLToPath(new URL('../cli/canvas-mcp-schemas.mjs', import.meta.url))
const vite = await createServer({ server: { middlewareMode: true }, appType: 'custom' })

try {
	const { CanvasToolInputSchemas, toCanvasToolInputJsonSchema } = await vite.ssrLoadModule('/shared/canvas-contract.ts')
	const schemas = Object.fromEntries(
		Object.entries(CanvasToolInputSchemas).map(([name, schema]) => [name, toCanvasToolInputJsonSchema(schema)])
	)
	await writeFile(
		outputPath,
		`// Generated by scripts/generate-canvas-mcp-schemas.mjs from shared/canvas-contract.ts.\nexport const canvasToolInputSchemas = Object.freeze(${JSON.stringify(schemas, null, '\t')})\n`
	)
} finally {
	await vite.close()
}
